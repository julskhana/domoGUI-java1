    public synchronized void actualizarEstadoLEDs(int n){
        if(n==1){           
            try {
                lbled1.setIcon(new ImageIcon("img/led_green2.png"));
                Thread.sleep(1000);                 
                lbled1.setIcon(new ImageIcon("img/led_green1.png"));
            } catch(InterruptedException ex) {
                Thread.currentThread().interrupt();
            }
        }else if(n==2){
            try {
                lbled2.setIcon(new ImageIcon("img/led_blue2.png"));
                Thread.sleep(1000);                 
                lbled2.setIcon(new ImageIcon("img/led_blue1.png"));
            } catch(InterruptedException ex) {
                Thread.currentThread().interrupt();
            }
        }
    }

