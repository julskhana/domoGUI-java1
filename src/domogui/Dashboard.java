    public void nDispositivos(ArrayList<Sensor> s, ArrayList<Actuador> a){
        int ns=0, na=0, i = 0;
        for(i = 0; i<s.size();i++){
            ns++;
        }
        for (i =0; i<a.size();i++){
            na++;
        }
        tfnAct.setText(String.valueOf(na));
        tfnSensores.setText(String.valueOf(ns));
        
    }
    
    public void tempMediaCasa(ArrayList<Sensor> st){
        int temp, tempTotal=0, n=0;
        String tipo="temp";
        for (int i=0;i<st.size();i++){
            if ((st.get(i).getTipo().equalsIgnoreCase(tipo))){
                n++;
                temp = (int) st.get(i).getValAct();
                tempTotal = tempTotal + temp;
            }
        }
        if (n==0){
            tfTemp.setText("0");
        }else{
            tfTemp.setText(String.valueOf(tempTotal/n));
        }
    }
    
        private void actualizarPuertos(){
        Vector puertos = ConexionSerial.obtenerPuertos();
        cbPuerto.removeAllItems();
        for (int i = 0; i < puertos.size(); i++) 
            cbPuerto.addItem(puertos.get(i));
    }
    
